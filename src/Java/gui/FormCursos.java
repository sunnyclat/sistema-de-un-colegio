private boolean validar(){
    //validar txtTitulo entre 2 y 20 caracteres
    
    if(!new Validator(txtMat).length(2,20))
        
        return false;
    
    
    //validar txtProfesor entre 2 y 25 caracteres
    
    
     if(!new Validator(txtProfesor).length(2,25)) 
         
         return false;
     
     
    return true;
    
}
    
    

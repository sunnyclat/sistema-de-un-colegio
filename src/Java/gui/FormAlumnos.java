    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        
        if(validar()){
            
            //recordar cambiar el parametro del combobox de string al nombre del enumerador. Sino tirara error de conversion a string.
            //En este caso, le ponemos el nombre del generic (<curso>)
            
            Alumnos alumno= new Alumnos(
                    
                    txtNombAlum.getText(),
                    txtApeAlum.getText(),
                    Integer.parseInt(txtEdadAlum.getText()),
                    cmbCursos.getItemAt(cmbCursos.getSelectedIndex() )
            //si tenemos un error en el getitemat es probable que no hayamos importado la clase curso.
                    
            );    
                    
              ar.save(alumno);
            limpiar();
            
            cargarTable(); //va adentro del validar
            
            
            lblInfo.setText("Se dio de alta un alumno codigo: "+alumno.getCodigo());
            
            
       
            
        }else{
            lblInfo.setText("No se pudo ingresar el alumno.");
        }     
                    
                    
        
        
         
        
      
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed


    public boolean validar(){
        
        
        
        if(!new Validator(txtNombAlum).length(2,25)) return false;
        
        
        if(!new Validator(txtApeAlum).length(2,25)) return false;
        
        
        if(!new Validator(txtEdadAlum).isInteger(18,70)) return false;
        
        
        
        
        return true;
    }
    
    
    private void limpiar(){
        
        txtNombAlum.setText("");
        txtApeAlum.setText("");
        txtEdadAlum.setText("");
        cmbCursos.setSelectedIndex(0);
          txtNombAlum.requestFocus();
    }
    
    
    
